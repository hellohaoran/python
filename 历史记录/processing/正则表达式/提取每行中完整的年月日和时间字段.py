import re
l = ['asd45451年5月25日16时25分60秒fdgs','45451年5月25日16时25分58秒','45451年5月25日50时25分0秒','4551年25月25日16时25分58秒']
for i in l:
    ret = re.match('.*?(\d{4}年[0-1]?[0-9]月[0-3]?[0-9]日[0-2]?[0-9]时[0-6]?[0-9]分)',i)
    if ret:
        print('{}匹配成功！'.format(ret.group(1)))
    else:
        print('{}匹配不成功！'.format(i))

